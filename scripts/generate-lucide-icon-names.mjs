#!/usr/bin/env node
/**
 * Generates lib/lucide-icon-names.ts from lucide-react exports.
 * Run after upgrading lucide-react: node scripts/generate-lucide-icon-names.mjs
 */
import * as Lucide from "lucide-react";
import { writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");

const all = Object.keys(Lucide);
const icons = [];
for (const k of all) {
  if (!/^[A-Z]/.test(k)) continue;
  if (k.startsWith("Lucide") && k.length > 6) {
    const without = k.slice(6);
    if (all.includes(without)) continue;
  }
  if (k.endsWith("Icon") && k !== "LucideIcon") {
    const base = k.slice(0, -4);
    if (all.includes(base)) continue;
  }
  const v = Lucide[k];
  if (typeof v !== "object" || v === null || !v.$$typeof) continue;
  if (typeof v.render !== "function") continue;
  icons.push(k);
}
icons.sort();

const lines = [];
const perLine = 8;
for (let i = 0; i < icons.length; i += perLine) {
  lines.push(
    "  " +
      icons
        .slice(i, i + perLine)
        .map((n) => `"${n}"`)
        .join(", ") +
      ","
  );
}

const out = `/**
 * All Lucide React icon component names from lucide-react.
 * Used for the badge icon picker to search across the full icon set.
 * Generated by scripts/generate-lucide-icon-names.mjs — re-run after upgrading lucide-react.
 */
export const LUCIDE_ICON_NAMES = [
${lines.join("\n")}
] as const;

const LUCIDE_ICON_NAME_SET = new Set<string>(LUCIDE_ICON_NAMES);

/** True if \`name\` is a known Lucide icon export (PascalCase). */
export function isValidLucideIconName(name: string): boolean {
  return LUCIDE_ICON_NAME_SET.has(name);
}

/** Convert PascalCase to "Title case" for display (e.g. RocketLaunch -> "Rocket launch") */
export function lucideIconToLabel(name: string): string {
  return name
    .replace(/([A-Z])/g, " $1")
    .trim()
    .toLowerCase()
    .replace(/^\\w/, (c) => c.toUpperCase());
}

/** Options for SearchableSelect — all Lucide icons with searchable labels */
export const BADGE_ICON_OPTIONS = [
  { value: "", label: "None (text only)" },
  ...LUCIDE_ICON_NAMES.map((name) => ({
    value: name,
    label: lucideIconToLabel(name),
  })),
];
`;

writeFileSync(join(root, "lib/lucide-icon-names.ts"), out, "utf8");
console.log(`Wrote lib/lucide-icon-names.ts (${icons.length} icons)`);
